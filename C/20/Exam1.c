#include <stdio.h>

// 산술 연산자
int main() {
  int a = 10;
  int b = 4;

  printf("a + b = %d\n", a + b); // a + b = 14
  printf("a - b = %d\n", a - b); // a - b = 6 
  printf("a * b = %d\n", a * b); // a * b = 40
  printf("a / b = %d\n", a / b); // a / b = 2 (정수 나눗셈)
  printf("a % b = %d\n", a % b); // a % b = 2 (나머지 연산)
  return 0;
}