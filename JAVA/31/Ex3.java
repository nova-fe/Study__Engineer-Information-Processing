import java.util.Scanner;

public class Ex3 {
  public static void main(String[] args) {
      Scanner sc = new Scanner(System.in);
      int a = sc.nextInt();
      // next(): 공백이 생기기 전까지 입력받은 문자열을 입력
      // nextLine(): 줄바꿈(엔터) 전까지 쓴 문자열을 모두 입력
      // nextInt(): int 데이터를 입력
      // nextFloat(): float 데이터를 입력
      System.out.println(a);
  }
}
