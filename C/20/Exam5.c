#include <stdio.h>

// 비트 연산자
int main() {
  int a = 5;
  int b = 3;

  // 둘 다 1일 때만 1
  printf("a & b = %d\n", a & b);  // a & b = 1 (0101 & 0011 = 0001)
  // 둘 중 하나라도 1이면 1
  printf("a | b = %d\n", a | b);  // a | b = 7 (0101 | 0011 = 0111)
  // 둘이 다를 때만 1
  printf("a ^ b = %d\n", a ^ b); // a ^ b = 6 (0101 ^ 0011 = 0110)
  // 비트 반전
  printf("~a = %d\n", ~a); // ~a = -6 (비트 반전: 0101 -> 1010, 2의 보수로 -6)

  // -a는 a의 음수화 (2의 보수)
  printf("-a = %d\n", ~a+1); // -a = -5 (단순 음수화)
  return 0;
}